# tipo numerico
numero = int(input("Digite um numero: "))
numero2 = int(input("Digite outro numero:"))
soma = numero + numero2
subtracao = numero - numero2
divisao = numero/numero2
multiplicacao = numero*numero2
divisaointeira = numero//numero2     # inteiro
elevado = numero**numero2
resto = numero%numero2

print(soma, subtracao, divisao, multiplicacao, divisaointeira, elevado, resto)